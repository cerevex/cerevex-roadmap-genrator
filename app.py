from flask import Flask, request, jsonify
import openai
import os

app = Flask(__name__)
openai.api_key = os.getenv("OPENAI_API_KEY")

@app.route("/generate_roadmap", methods=["POST"])
def generate_roadmap():
    data = request.json
    mission = data.get("mission")
    if not mission:
        return jsonify({"error": "Mission is required"}), 400

    prompt = f"Create a detailed step-by-step roadmap for the following mission:\n\n'{mission}'"

    response = openai.ChatCompletion.create(
        model="gpt-4",
        messages=[
            {"role": "system", "content": "You are a helpful assistant that creates step-by-step roadmaps."},
            {"role": "user", "content": prompt}
        ],
        max_tokens=700,
        temperature=0.7
    )

    roadmap = response.choices[0].message['content']
    roadmap += "\n\n---\nGenerated by Cerevex AI ðŸ¤–"
    return jsonify({"roadmap": roadmap})

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=10000)
